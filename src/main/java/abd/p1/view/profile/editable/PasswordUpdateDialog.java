    private void agreeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agreeButtonActionPerformed
        if (!passField1.getText().isEmpty() && passField1.getText().equals(passField2.getText())) {
            controller.updatePassword(passField1.getText());

            JOptionPane.showMessageDialog(this,
                    "Contrase単a modificada correctamente",
                    "",
                    JOptionPane.INFORMATION_MESSAGE);

            this.setVisible(false);
            this.dispose();
        } else {
            errorLabel.setVisible(true);
        }
    }//GEN-LAST:event_agreeButtonActionPerformed

